package net.finch.calendar;

public class MainBottomChapterObject {
    private int imgRes;
    String text;

    public MainBottomChapterObject(int imgRes, String text) {
        this.imgRes = imgRes;
        this.text = text;
    }

    public int getImgRes() {
        return imgRes;
    }

    public String getText() {
        return text;
    }
}
